for (const [locale, strings] of Object.entries(STRINGS)) {
  const filePath = path.join(LOCALES_DIR, `${locale}.json`)
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'))
  if (!data.audit) data.audit = {}
  for (const [section, values] of Object.entries(strings)) {
    if (!data.audit[section]) data.audit[section] = {}
    Object.assign(data.audit[section], values)
  }
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8')
  console.log(`Updated ${locale}.json`)
}
